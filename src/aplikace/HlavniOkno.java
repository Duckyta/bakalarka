    private void smazDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_smazDataBtnActionPerformed
        switch (vyberDatabazeComboBox.getSelectedIndex()) {
            case 0:
                if (inMemCheckBox.isSelected() == true) {
                    try {
                        if (pr.getConnection() != null) {
                            kody.metodyOrcl.dropTables(pr.getConnection());
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(HlavniOkno.class
                                .getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    try {
                        if (pr.getConnection() != null) {
                            kody.metodyOrcl.dropTables(pr.getConnection());

                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(HlavniOkno.class
                                .getName()).log(Level.SEVERE, null, ex);
                    }
                }
                break;
            case 1:
                try {
                    if (pr.getConnection() != null) {
                        try {
                            kody.metodyOstatni.smazProstresti(pr.getConnection());
                        } catch (SQLException ex) {
                            Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case 2:
                try {
                    if (pr.getConnection() != null) {
                        try {
                            kody.metodyOstatni.smazProstrestiMySQL(pr.getConnection());
                        } catch (SQLException ex) {
                            Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case 3: {
                try {
                    if (pr.getConnection() != null) {
                        pr.conn.close();
                        Class.forName("org.postgresql.Driver");
                        String url = "jdbc:postgresql://";
                        url += pr.postgreServer;
                        url += ":";
                        url += pr.postgrePort;
                        url += "/";
                        url += pr.postgreDatabaze;
                        String uziv = pr.postgreUziv;
                        String hesl = pr.postgreHesl;
                        pr.conn = DriverManager.getConnection(url, uziv, hesl);
                        kody.metodyOstatni.smazProstrestiPostge(pr.conn);

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;
        }
    }//GEN-LAST:event_smazDataBtnActionPerformed

