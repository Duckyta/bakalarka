    private void vytvoritDatabaziBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vytvoritDatabaziBtnActionPerformed
        if (vyberDatabazeComboBox.getSelectedIndex() == 0) {
            if (inMemCheckBox.isSelected() == true) {
                try {
                    if (pr.getConnection() != null) {
                        kody.metodyOrcl.createINmemTables(pr.getConnection());
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                    
                }
            } else if (inMemCheckBox.isSelected() == false) {
                try {
                    if (pr.getConnection() != null) {
                        kody.metodyOrcl.createTables(pr.getConnection());
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                    
                }
            }

        } else if (vyberDatabazeComboBox.getSelectedIndex() == 1) {
            if (inMemCheckBox.isSelected() == true) {
                try {
                    if (pr.getConnection() != null) {
                        kody.metodyOstatni.vytvorInMemTabulky(pr.getConnection());
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (inMemCheckBox.isSelected() == false) {
                try {
                    if (pr.getConnection() != null) {
                        kody.metodyOstatni.vytvorTabulkyMicrosoftSQL(pr.getConnection());
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } else if (vyberDatabazeComboBox.getSelectedIndex() == 2) {
            try {
                if (pr.getConnection() != null) {
                    kody.metodyOstatni.vytvorTabulkyMySQL(pr.getConnection());
                }
            } catch (SQLException ex) {
                Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (vyberDatabazeComboBox.getSelectedIndex() == 3) {
            try {
                if (pr.getConnection() != null) {
                    kody.metodyOstatni.vytvorTabulkyPostgre(pr.getConnection());
                }
            } catch (SQLException ex) {
                Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_vytvoritDatabaziBtnActionPerformed

